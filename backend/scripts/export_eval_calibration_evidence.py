from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any


class CalibrationEvidenceError(RuntimeError):
    pass


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Export a versionable, path-safe Markdown summary from an eval calibration "
            "manifest. The generated evidence intentionally excludes local dataset and "
            "report paths."
        )
    )
    parser.add_argument(
        "--manifest",
        type=Path,
        default=Path("evals/reports/calibration/manifest.json"),
        help="Calibration manifest generated by run_eval_calibration.py.",
    )
    parser.add_argument(
        "--output",
        type=Path,
        help="Markdown evidence file to write. Omit to print to stdout.",
    )
    parser.add_argument(
        "--title",
        default="Real-Dataset Calibration Evidence",
        help="Markdown title for the evidence document.",
    )
    parser.add_argument(
        "--command",
        action="append",
        default=[],
        help="Command used to produce the manifest. May be repeated.",
    )
    parser.add_argument(
        "--allow-failed",
        action="store_true",
        help="Export evidence even when the manifest failed. Without this, failed manifests error.",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    try:
        manifest = load_manifest(args.manifest)
        evidence = build_evidence(
            manifest=manifest,
            title=args.title,
            commands=tuple(args.command),
            require_passed=not args.allow_failed,
        )
    except CalibrationEvidenceError as exc:
        print(f"Calibration evidence error: {exc}", file=sys.stderr)
        return 2

    markdown = render_markdown(evidence)
    if args.output is None:
        print(markdown, end="")
    else:
        args.output.parent.mkdir(parents=True, exist_ok=True)
        args.output.write_text(markdown, encoding="utf-8")
    return 0


def load_manifest(path: Path) -> dict[str, Any]:
    if not path.is_file():
        raise CalibrationEvidenceError(f"Manifest not found: {path}")
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        raise CalibrationEvidenceError(f"Manifest is not valid JSON: {path}") from exc
    if not isinstance(payload, dict):
        raise CalibrationEvidenceError("Manifest root must be an object")
    return payload


def build_evidence(
    *,
    manifest: dict[str, Any],
    title: str,
    commands: tuple[str, ...],
    require_passed: bool,
) -> dict[str, Any]:
    targets = manifest.get("targets")
    if not isinstance(targets, list) or not targets:
        raise CalibrationEvidenceError("Manifest must contain at least one target")

    manifest_passed = manifest.get("passed")
    if manifest_passed is not True and require_passed:
        raise CalibrationEvidenceError("Manifest did not pass; rerun with --allow-failed to export")

    evidence_targets = [summarize_target(target) for target in targets]
    return {
        "title": title,
        "status": "PASS" if manifest_passed is True else "FAIL",
        "started_at": string_value(manifest.get("started_at")),
        "completed_at": string_value(manifest.get("completed_at")),
        "max_records": int_or_none(manifest.get("max_records")),
        "max_cases": int_or_none(manifest.get("max_cases")),
        "target_count": int_or_none(manifest.get("target_count")) or len(evidence_targets),
        "metric_gates": summarize_metric_gates(manifest.get("metric_gates")),
        "commands": [command for command in commands if command.strip()],
        "targets": evidence_targets,
    }


def summarize_target(raw_target: Any) -> dict[str, Any]:
    if not isinstance(raw_target, dict):
        raise CalibrationEvidenceError("Every manifest target must be an object")
    dataset = raw_target.get("dataset")
    metrics = raw_target.get("metrics")
    gates = raw_target.get("gates", [])
    if not isinstance(dataset, dict):
        raise CalibrationEvidenceError("Every manifest target must include dataset provenance")
    if not isinstance(metrics, dict):
        raise CalibrationEvidenceError("Every manifest target must include metrics")
    if not isinstance(gates, list):
        raise CalibrationEvidenceError("Target gates must be a list when present")

    return {
        "key": string_value(raw_target.get("key")),
        "suite": string_value(raw_target.get("suite")),
        "description": string_value(raw_target.get("description")),
        "passed": raw_target.get("passed") is True,
        "report_passed": raw_target.get("report_passed") is True,
        "gates_passed": raw_target.get("gates_passed") is True,
        "case_count": int_or_none(raw_target.get("case_count")),
        "metrics": numeric_items(metrics),
        "gates": [summarize_gate(gate) for gate in gates],
        "dataset": {
            "profile": string_value(dataset.get("profile")),
            "suite": string_value(dataset.get("suite")),
            "records": int_or_none(dataset.get("records")),
            "reused": dataset.get("reused") is True,
            "source": string_value(dataset.get("source")),
            "source_url": string_value(dataset.get("source_url")),
            "license_note": string_value(dataset.get("license_note")),
        },
    }


def summarize_metric_gates(raw_gates: Any) -> list[dict[str, Any]]:
    if raw_gates is None:
        return []
    if not isinstance(raw_gates, list):
        raise CalibrationEvidenceError("Manifest metric_gates must be a list")
    return [summarize_gate(gate) for gate in raw_gates]


def summarize_gate(raw_gate: Any) -> dict[str, Any]:
    if not isinstance(raw_gate, dict):
        raise CalibrationEvidenceError("Metric gates must be objects")
    return {
        "target": string_value(raw_gate.get("target")),
        "name": string_value(raw_gate.get("name")),
        "minimum": float_or_none(raw_gate.get("minimum")),
        "actual": float_or_none(raw_gate.get("actual")),
        "passed": raw_gate.get("passed") if isinstance(raw_gate.get("passed"), bool) else None,
    }


def render_markdown(evidence: dict[str, Any]) -> str:
    lines = [
        f"# {evidence['title']}",
        "",
        f"Status: {evidence['status']}",
        "",
        "| Field | Value |",
        "| --- | --- |",
        f"| Started at | {markdown_value(evidence['started_at'])} |",
        f"| Completed at | {markdown_value(evidence['completed_at'])} |",
        f"| Targets | {evidence['target_count']} |",
        f"| Max records | {markdown_value(evidence['max_records'])} |",
        f"| Max cases | {markdown_value(evidence['max_cases'])} |",
        "",
    ]
    commands = evidence["commands"]
    if commands:
        lines.extend(["## Reproduction", ""])
        for command in commands:
            lines.extend(["```bash", command, "```", ""])

    metric_gates = evidence["metric_gates"]
    if metric_gates:
        lines.extend(
            [
                "## Metric Gates",
                "",
                "| Target | Metric | Minimum |",
                "| --- | --- | ---: |",
            ]
        )
        for gate in metric_gates:
            lines.append(
                f"| {markdown_value(gate['target'])} | {gate['name']} | "
                f"{markdown_value(gate['minimum'])} |"
            )
        lines.append("")

    lines.extend(
        [
            "## Targets",
            "",
            "| Target | Suite | Status | Cases | Dataset | Records | Source URL |",
            "| --- | --- | --- | ---: | --- | ---: | --- |",
        ]
    )
    for target in evidence["targets"]:
        dataset = target["dataset"]
        source_url = dataset["source_url"] or dataset["source"]
        row_template = (
            "| {key} | {suite} | {status} | {cases} | {profile} | {records} | {source_url} |"
        )
        lines.append(
            row_template.format(
                key=target["key"],
                suite=target["suite"],
                status="PASS" if target["passed"] else "FAIL",
                cases=markdown_value(target["case_count"]),
                profile=dataset["profile"],
                records=markdown_value(dataset["records"]),
                source_url=source_url,
            )
        )
    lines.append("")

    for target in evidence["targets"]:
        dataset = target["dataset"]
        lines.extend(
            [
                f"## {target['key']} Metrics",
                "",
                target["description"],
                "",
                "| Metric | Score |",
                "| --- | ---: |",
            ]
        )
        for name, value in target["metrics"]:
            lines.append(f"| {name} | {format_number(value)} |")
        lines.extend(
            [
                "",
                "| Provenance | Value |",
                "| --- | --- |",
                f"| Dataset profile | {dataset['profile']} |",
                f"| Dataset suite | {dataset['suite']} |",
                f"| Reused local sample | {str(dataset['reused']).lower()} |",
                f"| Source | {dataset['source']} |",
                f"| Source URL | {markdown_value(dataset['source_url'])} |",
                f"| License note | {dataset['license_note']} |",
                "",
            ]
        )
        if target["gates"]:
            lines.extend(
                [
                    "| Gate | Minimum | Actual | Status |",
                    "| --- | ---: | ---: | --- |",
                ]
            )
            for gate in target["gates"]:
                status = gate["passed"]
                status_text = "PASS" if status is True else "FAIL" if status is False else "-"
                lines.append(
                    f"| {gate['name']} | {markdown_value(gate['minimum'])} | "
                    f"{markdown_value(gate['actual'])} | {status_text} |"
                )
            lines.append("")

    lines.extend(
        [
            "## Notes",
            "",
            "- Local dataset paths and report paths are intentionally omitted.",
            "- Source URLs and license notes come from the calibration manifest.",
            "- Generated datasets and raw reports remain ignored under `evals/`.",
            "",
        ]
    )
    return "\n".join(lines)


def numeric_items(metrics: dict[str, Any]) -> list[tuple[str, float]]:
    items: list[tuple[str, float]] = []
    for name, value in sorted(metrics.items()):
        if isinstance(value, int | float):
            items.append((name, float(value)))
    return items


def string_value(value: Any) -> str:
    return value if isinstance(value, str) else ""


def int_or_none(value: Any) -> int | None:
    return value if isinstance(value, int) and not isinstance(value, bool) else None


def float_or_none(value: Any) -> float | None:
    return float(value) if isinstance(value, int | float) and not isinstance(value, bool) else None


def markdown_value(value: object) -> str:
    if value is None or value == "":
        return "-"
    if isinstance(value, float):
        return format_number(value)
    return str(value)


def format_number(value: float) -> str:
    return f"{value:.4g}"


if __name__ == "__main__":
    sys.exit(main())
